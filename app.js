ALL_TYPES.forEach(type => {
  if (!typeCounts[type]) return;
  const btn = document.createElement('button');
  btn.className = 'type-btn';
  btn.dataset.type = type;
  btn.innerHTML = `
    <div class="type-dot" style="background:${TYPE_COLORS[type]}"></div>
    <span class="type-name">${type}</span>
    <span class="type-count">${typeCounts[type]||0}</span>
  `;
  btn.addEventListener('click', () => toggleType(type, btn));
  filtersEl.appendChild(btn);
});

function toggleType(type, btn) {
  if (activeTypes.has(type)) {
    activeTypes.delete(type);
    btn.classList.add('off');
    markersByType[type]?.forEach(m => layerGroup.removeLayer(m));
  } else {
    activeTypes.add(type);
    btn.classList.remove('off');
    markersByType[type]?.forEach(m => layerGroup.addLayer(m));
  }
  updateStatTotal();
}

