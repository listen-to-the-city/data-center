const CATEGORY_TO_TYPE = Object.fromEntries(
  Object.entries(CAT_LABELS).map(([k, v]) => [k, v[0]])
);
function normalizeDcType(dc) {
  const existing = (dc?.type ?? '').toString().trim();
  if (existing) return existing;

  const cat = (dc?.category ?? dc?.cat_raw ?? '').toString().trim();
  if (cat && CATEGORY_TO_TYPE[cat]) return CATEGORY_TO_TYPE[cat];
  if (cat && TYPE_COLORS[cat]) return cat;

  const sector = (dc?.sector ?? '').toString();
  if (sector.includes('공공')) return '공공기관 DC';

  const y = parseInt((dc?.year ?? '').toString(), 10);
  if (Number.isFinite(y) && y > new Date().getFullYear()) return '계획/예정';

  return cat || '기타';
}

